<template>
  <div class="carManager">
    <div class="crumbs">
      <el-breadcrumb separator="/">
        <el-breadcrumb-item>
          <i class="el-icon-lx-cascades"></i> 用户管理
        </el-breadcrumb-item>
      </el-breadcrumb>
    </div>
    <div class="container">
      <div class="handle-box">
      </div>
      <el-table
        :data="tableData"
        border
        class="table"
        ref="multipleTable"
      >
        <el-table-column type="selection" width="50" align="center" class-name="table"></el-table-column>
        <el-table-column type="index" width="50" align="center" label="序号" class-name="table"></el-table-column>
        <el-table-column prop="userId" label="用户名" width="100" class-name="table"></el-table-column>
		<el-table-column prop="deviceId" label="设备" width="100" class-name="table"></el-table-column>
		<el-table-column prop="waterId" label="类型" class-name="table"></el-table-column>
		<el-table-column prop="capacity" label="容量" class-name="table"></el-table-column>
		<el-table-column prop="price" label="价格" class-name="table"></el-table-column>
      </el-table>
      <div class="pagination">
        <el-pagination
          background
          @size-change="handleSizeChange"
          @current-change="handleCurrentChange"
          :current-page="cur_page"
          :page-size="pagesize"
          :total="totalRecords"
          :page-sizes="[5, 10, 20, 40]"
          layout="total, sizes, prev, pager, next, jumper"
        ></el-pagination>
      </div>
    </div>
  </div>
